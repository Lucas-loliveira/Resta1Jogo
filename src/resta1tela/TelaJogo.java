    private boolean checkMov(int x, int y, char dir) {
        try {
            switch (dir) {

                case 'd': //baixo
                    return this.bord[x + 1][y] == 1 && this.bord[x + 2][y] == 0;

                case 'u': //cima
                    return this.bord[x - 1][y] == 1 && this.bord[x - 2][y] == 0;

                case 'r': //direita
                    return this.bord[x][y + 1] == 1 && this.bord[x][y + 2] == 0;

                case 'l': // esquerda
                    return this.bord[x][y - 1] == 1 && this.bord[x][y - 2] == 0;
                default:
                    return false;
            }
        } catch (ArrayIndexOutOfBoundsException e) {

            return false;
        }

    }

    private boolean validbord() {
        int resp;
        int i, j;
        for (i = 0; i < bord.length; i++) {
            for (j = 0; j < bord.length; j++) {
                if (bord[i][j] == 0) {
                    resp = solver.analisemov(bord, i, j, 0);
                    if (resp < 5) {
                        return true;
                    }
                }
            }
        }

        return false;
    }


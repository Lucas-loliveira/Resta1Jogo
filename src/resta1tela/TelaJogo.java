    private void endGame() {
        if (!validbord()) {

            if (solver.numbPieces(bord) == 1) {
                JOptionPane.showMessageDialog(null, "PARABÉNS VOCÊ GANHOU!!");

            } else {
                int i = JOptionPane.showConfirmDialog(null,
                        "Não existe mais movimentos possíveis, deseja reiniciar o jogo?",
                        "FIM DE JOGO", JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
                if (i == JOptionPane.YES_OPTION) {
                    reGame();
                }
            }
        }
    }


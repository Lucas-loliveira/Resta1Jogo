    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int i, j;
        sol.clear();
        jLabel15.setText("Resolvendo...");
        int[][] bord2 = new int[7][7];
        
        for (i = 0; i < bord.length; i++) {
            for (j = 0; j < bord.length; j++) {
                bord2[i][j] = bord[i][j];
            }
        }
        sol = solver.solve(bord2);


        if (!sol.isEmpty()) {
            jLabel15.setText("<html>");
            for (i = 0; i < sol.size(); i++) {
                jLabel15.setText(jLabel15.getText() + "<br/>" + (i + 1) + ". Mova: " + trasfLet(sol.get(i).getxo()) + "" + (sol.get(i).getyo() + 1));
                jLabel15.setText(jLabel15.getText() + " Para: " + trasfLet(sol.get(i).getxd()) + "" + (sol.get(i).getyd() + 1));
            }

            jButton24.setEnabled(true);

        } else {
            jLabel15.setText("Não existe solução");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

